const first = 2;
const second = "2";

//double == check only value
if(first == second){
    console.log("condition is true");
}
else{
    console.log("condition is false");
}



//triple === check value and type

if(first === second){
    console.log("condition is true");
}
else{
    console.log("condition is false");
}

//TRIPLE === is recommended